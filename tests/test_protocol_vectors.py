def frame(byte2, byte3, byte6, byte7, frame2, byte4=0x00, byte8=0x00, byte5=0x00):
    return [
        0x83,
        0x06,
        byte2,
        byte3,
        byte4,
        byte5,
        byte6,
        byte7,
        byte8,
        0x00,
        0x00,
        0x00,
        0x00,
        byte2 ^ byte3 ^ byte4 ^ byte5 ^ byte6 ^ byte7 ^ byte8,
        *frame2,
    ]


def main():
    mode = [0x00, 0x06, 0x00, 0x00, 0x08, 0x00, 0x0E]
    auto = [0x00, 0x17, 0x00, 0x00, 0x08, 0x00, 0x1F]
    eco_on = [0x20, 0x0C, 0x00, 0x00, 0x08, 0x00, 0x24]
    eco_off = [0x00, 0x0C, 0x00, 0x00, 0x08, 0x00, 0x04]
    sleep = [0x00, 0x03, 0x00, 0x00, 0x08, 0x00, 0x0B]
    boost = [0x00, 0x04, 0x00, 0x00, 0x08, 0x00, 0x0C]
    display = [0x00, 0x00, 0x00, 0x00, 0x08, 0x00, 0x08]
    vertical_swing = [0x00, 0x07, 0x00, 0x00, 0x08, 0x00, 0x0F]
    horizontal_swing = [0x00, 0x08, 0x00, 0x00, 0x08, 0x00, 0x00]

    assert frame(0x00, 0x73, 0x91, 0x1E, mode) == [
        0x83, 0x06, 0x00, 0x73, 0x00, 0x00, 0x91, 0x1E, 0, 0, 0, 0, 0, 0xFC, *mode
    ]
    assert frame(0x01, 0x84, 0x91, 0x20, mode) == [
        0x83, 0x06, 0x01, 0x84, 0x00, 0x00, 0x91, 0x20, 0, 0, 0, 0, 0, 0x34, *mode
    ]
    assert frame(0x01, 0x71, 0x91, 0x1E, auto, 0x80) == [
        0x83, 0x06, 0x01, 0x71, 0x80, 0x00, 0x91, 0x1E, 0, 0, 0, 0, 0, 0x7F, *auto
    ]
    assert frame(0x03, 0xA2, 0x91, 0x05, eco_on) == [
        0x83, 0x06, 0x03, 0xA2, 0x00, 0x00, 0x91, 0x05, 0, 0, 0, 0, 0, 0x35, *eco_on
    ]
    assert frame(0x00, 0xA2, 0x91, 0x05, eco_off) == [
        0x83, 0x06, 0x00, 0xA2, 0x00, 0x00, 0x91, 0x05, 0, 0, 0, 0, 0, 0x36, *eco_off
    ]
    assert frame(0x08, 0xA2, 0x94, 0x22, sleep) == [
        0x83, 0x06, 0x08, 0xA2, 0x00, 0x00, 0x94, 0x22, 0, 0, 0, 0, 0, 0x1C, *sleep
    ]
    assert frame(0x00, 0xA2, 0x94, 0x22, sleep) == [
        0x83, 0x06, 0x00, 0xA2, 0x00, 0x00, 0x94, 0x22, 0, 0, 0, 0, 0, 0x14, *sleep
    ]
    assert frame(0x01, 0x02, 0x97, 0x39, boost, byte5=0x90) == [
        0x83, 0x06, 0x01, 0x02, 0x00, 0x90, 0x97, 0x39, 0, 0, 0, 0, 0, 0x3D, *boost
    ]
    assert frame(0x00, 0xA2, 0x97, 0x34, boost) == [
        0x83, 0x06, 0x00, 0xA2, 0x00, 0x00, 0x97, 0x34, 0, 0, 0, 0, 0, 0x01, *boost
    ]
    assert frame(0x00, 0xA2, 0xB1, 0x06, display) == [
        0x83, 0x06, 0x00, 0xA2, 0x00, 0x00, 0xB1, 0x06, 0, 0, 0, 0, 0, 0x15, *display
    ]
    assert frame(0x80, 0xA2, 0x94, 0x23, vertical_swing, byte8=0x40) == [
        0x83, 0x06, 0x80, 0xA2, 0x00, 0x00, 0x94, 0x23, 0x40, 0, 0, 0, 0, 0xD5, *vertical_swing
    ]
    assert frame(0x00, 0xA2, 0x94, 0x24, horizontal_swing, byte8=0x80) == [
        0x83, 0x06, 0x00, 0xA2, 0x00, 0x00, 0x94, 0x24, 0x80, 0, 0, 0, 0, 0x92, *horizontal_swing
    ]


if __name__ == "__main__":
    main()
